using System;

namespace MathApp
{
    public class MathOperations
    {
        // Method to add 10 to the input parameter
        public int AddTen(int number)
        {
            return number + 10;
        }

        // Method to multiply the input parameter by 2
        public int MultiplyByTwo(int number)
        {
            return number * 2;
        }

        // Method to subtract 5 from the input parameter
        public int SubtractFive(int number)
        {
            return number - 5;
        }
    }
}
